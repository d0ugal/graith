package protocol

// Cross-language protocol conformance (issue #1129).
//
// The wire protocol is defined twice: here in Go and, by hand, in Swift
// (`gui/shared/Sources/GraithProtocol/Messages.swift`). Nothing kept them in
// step, so drift was only ever caught by review. This file makes the Go side
// the single source of truth for the *shape* of the protocol: it enumerates
// every wire struct in messages.go and, via reflection, emits a language-neutral
// manifest of each type's JSON fields. That manifest is committed as a fixture
// (see manifest_test.go) and asserted against from both sides:
//
//   - Go (TestManifestUpToDate): regenerates the manifest and diffs it against
//     the committed fixture, so a change to messages.go that isn't regenerated
//     fails on the Go PR — which runs on *every* PR, closing the gap that
//     gui/ Swift CI is paths-filtered and would miss a Go-only change.
//   - Swift (ManifestConformanceTests): decodes the same fixture and verifies
//     that every type the manifest marks `required` has a Swift decoder that
//     accepts a synthesized instance — so when Go adds a required message the
//     Swift suite goes red until Messages.swift catches up.
//
// The manifest also records, per type, whether Swift is expected to model it
// (`required`), is a known-but-unfilled gap (`planned`), or will never need it
// (`na`, e.g. hook-CLI/daemon-internal or local-only messages). That turns the
// current 119-vs-32 divergence into an explicit, reviewable list rather than
// silent drift.

import (
	"bytes"
	"encoding/json"
	"fmt"
	"reflect"
	"sort"
	"strings"
)

// SwiftExpectation classifies whether the Swift client is expected to model a
// given Go wire type.
type SwiftExpectation string

const (
	// SwiftRequired: the Swift app models (or must model) this type. The
	// conformance test fails if Swift cannot decode a synthesized instance.
	SwiftRequired SwiftExpectation = "required"
	// SwiftPlanned: a client-relevant type Swift does not model yet — an
	// acknowledged gap. Not enforced against Swift, but recorded so the gap is
	// visible and reviewable.
	SwiftPlanned SwiftExpectation = "planned"
	// SwiftNA: a type a GUI/remote client never needs — hook-CLI↔daemon
	// internals, MCP proxy transport, or local-only (doctor/diagnostics/upgrade)
	// messages. Not modelled by Swift, and never expected to be.
	SwiftNA SwiftExpectation = "na"
)

// FieldType is a language-neutral description of a wire field's type. Kind is
// one of: string, int, float, bool, raw (a `json.RawMessage` opaque blob), map,
// array, object. array carries Elem; object carries Ref (the name of another
// manifest type).
//
// Known fidelity limits (none exercised by messages.go today, guarded so they
// can't silently appear): map loses its key/value types (kind "map" only); a
// fixed-length array loses its length (kind "array"); and describeFields rejects
// embedded/anonymous struct fields outright rather than mis-modelling
// encoding/json's field promotion. A plain `[]byte` is reported as "string"
// (encoding/json base64-encodes it), distinct from `json.RawMessage` ("raw").
type FieldType struct {
	Kind string     `json:"kind"`
	Elem *FieldType `json:"elem,omitempty"`
	Ref  string     `json:"ref,omitempty"`
}

// ManifestField is one JSON field of a wire type.
type ManifestField struct {
	// Name is the JSON key (the `json:"…"` tag), not the Go field name.
	Name string    `json:"name"`
	Type FieldType `json:"type"`
	// Optional is true when the field is a pointer or carries `omitempty` — i.e.
	// it may be absent from the wire form. Swift models these as Optional.
	Optional bool `json:"optional"`
}

// ManifestType is one wire struct: its Go name, its Swift expectation, and its
// fields in declaration order.
type ManifestType struct {
	Name      string           `json:"name"`
	Swift     SwiftExpectation `json:"swift"`
	SwiftType string           `json:"swift_type,omitempty"` // Swift type that satisfies it (required only)
	Fields    []ManifestField  `json:"fields"`
}

// Manifest is the committed, cross-language description of the wire protocol.
type Manifest struct {
	ProtocolVersion string `json:"protocol_version"`
	// Note is a human-oriented pointer back to the generator, embedded so the
	// committed fixture is self-documenting.
	Note  string         `json:"note"`
	Types []ManifestType `json:"types"`
}

