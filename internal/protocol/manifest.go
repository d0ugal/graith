// rawMessageType is encoding/json.RawMessage, treated as an opaque JSON blob.
var rawMessageType = reflect.TypeOf(json.RawMessage(nil))

// BuildManifest reflects over registeredTypes and produces the manifest,
// annotated with each type's Swift expectation. Types are sorted by name for a
// stable, diff-friendly fixture; fields keep declaration order.
func BuildManifest() (Manifest, error) {
	types := make([]ManifestType, 0, len(registeredTypes))
	seen := make(map[string]bool, len(registeredTypes))

	for _, v := range registeredTypes {
		rt := reflect.TypeOf(v)
		if rt.Kind() != reflect.Struct {
			return Manifest{}, fmt.Errorf("registered type %s is not a struct", rt.Name())
		}

		if seen[rt.Name()] {
			return Manifest{}, fmt.Errorf("type %s is registered more than once in registeredTypes", rt.Name())
		}

		seen[rt.Name()] = true

		ann, ok := swiftAnnotations[rt.Name()]
		if !ok {
			return Manifest{}, fmt.Errorf("type %s has no swiftAnnotations entry — classify it (required/planned/na)", rt.Name())
		}

		fields, err := describeFields(rt)
		if err != nil {
			return Manifest{}, fmt.Errorf("describe %s: %w", rt.Name(), err)
		}

		types = append(types, ManifestType{
			Name:      rt.Name(),
			Swift:     ann.exp,
			SwiftType: ann.swift,
			Fields:    fields,
		})
	}

	sort.Slice(types, func(i, j int) bool { return types[i].Name < types[j].Name })

	return Manifest{
		ProtocolVersion: Version,
		Note:            manifestNote,
		Types:           types,
	}, nil
}

// describeFields walks a struct's exported, JSON-serialized fields in
// declaration order. It always returns a non-nil slice so a fieldless struct
// (e.g. the no-payload request messages) marshals to `[]`, not `null` — cleaner
// to decode cross-language.
func describeFields(rt reflect.Type) ([]ManifestField, error) {
	fields := make([]ManifestField, 0, rt.NumField())
	for i := 0; i < rt.NumField(); i++ {
		f := rt.Field(i)
		if f.PkgPath != "" {
			continue // unexported
		}

		if f.Anonymous {
			// encoding/json promotes an untagged embedded struct's fields inline,
			// which describeFields does not model. Reject rather than emit a
			// misleading nested object. None exist today; this keeps it honest.
			return nil, fmt.Errorf("embedded/anonymous field %s is not supported by the manifest generator", f.Name)
		}

		name, omitempty, skip := jsonKey(f)
		if skip {
			continue
		}

		optional := omitempty

		ft := f.Type
		if ft.Kind() == reflect.Pointer {
			optional = true
			ft = ft.Elem()
		}

		desc, err := describeType(ft)
		if err != nil {
			return nil, fmt.Errorf("field %s: %w", f.Name, err)
		}

		fields = append(fields, ManifestField{
			Name:     name,
			Type:     desc,
			Optional: optional,
		})
	}

	return fields, nil
}

// jsonKey resolves a field's wire name and whether it carries omitempty. skip is
// true for `json:"-"`.
func jsonKey(f reflect.StructField) (name string, omitempty, skip bool) {
	tag := f.Tag.Get("json")
	if tag == "-" {
		return "", false, true
	}

	parts := strings.Split(tag, ",")

	name = parts[0]
	if name == "" {
		name = f.Name // Go default: the field name
	}

	for _, opt := range parts[1:] {
		if opt == "omitempty" {
			omitempty = true
		}
	}

	return name, omitempty, false
}

// describeType maps a (non-pointer) Go type to a language-neutral FieldType.
func describeType(t reflect.Type) (FieldType, error) {
	for t.Kind() == reflect.Pointer {
		t = t.Elem()
	}

	if t == rawMessageType {
		return FieldType{Kind: "raw"}, nil
	}

	switch t.Kind() {
	case reflect.String:
		return FieldType{Kind: "string"}, nil
	case reflect.Bool:
		return FieldType{Kind: "bool"}, nil
	case reflect.Int, reflect.Int8, reflect.Int16, reflect.Int32, reflect.Int64,
		reflect.Uint, reflect.Uint8, reflect.Uint16, reflect.Uint32, reflect.Uint64:
		return FieldType{Kind: "int"}, nil
	case reflect.Float32, reflect.Float64:
		return FieldType{Kind: "float"}, nil
	case reflect.Slice:
		if t.Elem().Kind() == reflect.Uint8 {
			// A plain []byte (that isn't json.RawMessage, handled above) is
			// base64-encoded as a JSON string by encoding/json.
			return FieldType{Kind: "string"}, nil
		}

		elem, err := describeType(t.Elem())
		if err != nil {
			return FieldType{}, err
		}

		return FieldType{Kind: "array", Elem: &elem}, nil
	case reflect.Array:
		elem, err := describeType(t.Elem())
		if err != nil {
			return FieldType{}, err
		}

		return FieldType{Kind: "array", Elem: &elem}, nil
	case reflect.Map:
		return FieldType{Kind: "map"}, nil
	case reflect.Struct:
		return FieldType{Kind: "object", Ref: t.Name()}, nil
	default:
		return FieldType{}, fmt.Errorf("unsupported kind %s", t.Kind())
	}
}

// MarshalManifest renders the manifest as stable, indented JSON with a trailing
// newline and HTML escaping disabled (so `<`/`>`/`&` in any future field survive
// verbatim). This is the exact byte form committed as the fixture.
func MarshalManifest(m Manifest) ([]byte, error) {
	var buf bytes.Buffer

	enc := json.NewEncoder(&buf)
	enc.SetEscapeHTML(false)
	enc.SetIndent("", "  ")

	if err := enc.Encode(m); err != nil {
		return nil, err
	}

	return buf.Bytes(), nil
}
