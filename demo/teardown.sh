#!/usr/bin/env bash
#
# teardown.sh — remove the isolated graith demo environment created by setup.sh.
#
# Safety: this only ever acts on the fixed, harness-owned `demo` profile, and
# only when the config, data, and (when present) runtime directories carry the
# same per-run ownership token. A missing runtime directory is reconstructible
# because it is ephemeral; every present target must already be owned before
# teardown contacts a daemon or purges a session.

set -euo pipefail

# Fixed, isolated profile. Clear any ambient graith session/agent env so every
# `gr` call targets the demo profile cleanly: an inherited GRAITH_SESSION_ID
# would retarget `gr status`, and agent-mode would otherwise change output and
# connection behavior outside this script's explicit flags.
export GRAITH_PROFILE=demo
export GR_AGENT_MODE=0
unset GRAITH_SESSION_ID GRAITH_SESSION_NAME GRAITH_AGENT_TYPE \
      GRAITH_WORKTREE_PATH GRAITH_REPO_PATH GRAITH_TOKEN 2>/dev/null || true

DEMO_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$DEMO_DIR/.." && pwd)"

# Fixed harness-owned paths — never from the environment.
CONFIG_DIR="${XDG_CONFIG_HOME:-$HOME/.config}/graith-demo"
CONFIG_FILE="$CONFIG_DIR/config.toml"
DATA_DIR="$HOME/.graith-demo"
OWNER_FILE=".graith-demo-owner"
CONFIG_PROOF_FILE=".graith-demo-config-proof"
CONFIG_MARKER="# Generated by demo/setup.sh owner="

DEMO_OS="$(uname -s)"
case "$DEMO_OS" in
	Darwin) RUNTIME_DIR="${XDG_RUNTIME_DIR:+$XDG_RUNTIME_DIR/graith-demo}" ;;
	Linux)
		if [ -z "${XDG_RUNTIME_DIR:-}" ]; then
			echo "refusing: XDG_RUNTIME_DIR must be set so demo runtime ownership can be verified." >&2
			exit 1
		fi
		RUNTIME_DIR="$XDG_RUNTIME_DIR/graith-demo"
		;;
	*)
		echo "refusing: demo teardown supports only Darwin and Linux." >&2
		exit 1
		;;
esac

GR="$REPO_ROOT/gr"
if [ ! -x "$GR" ]; then
	GR="$(command -v gr 2>/dev/null || true)"
fi

# Only our generated config carries a valid ownership token on line 1.
config_owner() {
	config_owner_from "$CONFIG_FILE"
}

config_owner_from() {
	config_file="$1"
	[ -f "$config_file" ] && [ ! -L "$config_file" ] || return 1
	line="$(LC_ALL=C sed -n '1p' "$config_file")" || return 1
	prefix="$CONFIG_MARKER"
	suffix=" — safe to delete. Isolated GRAITH_PROFILE=demo."
	case "$line" in
		"$prefix"*"$suffix") ;;
		*) return 1 ;;
	esac
	owner="${line#"$prefix"}"
	owner="${owner%"$suffix"}"
	[ "${#owner}" -eq 32 ] || return 1
	case "$owner" in
		*[!0-9a-f]*) return 1 ;;
	esac
	printf '%s\n' "$owner"
}

config_checksum() {
	LC_ALL=C cksum < "$1"
}

path_exists() {
	[ -e "$1" ] || [ -L "$1" ]
}

directory_owned_by() {
	dir="$1" owner="$2"
	[ -d "$dir" ] && [ ! -L "$dir" ] || return 1
	marker="$dir/$OWNER_FILE"
	[ -f "$marker" ] && [ ! -L "$marker" ] || return 1
	[ "$(cat "$marker")" = "$owner" ]
}

config_file_proven_by() {
	config_file="$1" proof="$2" owner="$3"
	[ "$(config_owner_from "$config_file" || true)" = "$owner" ] || return 1
	[ -f "$proof" ] && [ ! -L "$proof" ] || return 1
	checksum="$(config_checksum "$config_file")" || return 1
	[ "$(cat "$proof")" = "$owner $checksum" ]
}

config_directory_owned_by() {
	dir="$1" owner="$2"
	directory_owned_by "$dir" "$owner" || return 1
	config_file_proven_by "$dir/config.toml" "$dir/$CONFIG_PROOF_FILE" "$owner"
}

profile_paths_exist() {
	path_exists "$CONFIG_DIR" || path_exists "$DATA_DIR" ||
		{ [ -n "$RUNTIME_DIR" ] && path_exists "$RUNTIME_DIR"; }
}

durable_profile_owned_by() {
	owner="$1"
	[ -n "$owner" ] || return 1
	config_directory_owned_by "$CONFIG_DIR" "$owner" || return 1
	directory_owned_by "$DATA_DIR" "$owner"
}

profile_owned_by() {
	owner="$1"
	durable_profile_owned_by "$owner" || return 1
	if [ -n "$RUNTIME_DIR" ] && path_exists "$RUNTIME_DIR"; then
		directory_owned_by "$RUNTIME_DIR" "$owner" || return 1
	fi
}

profile_fully_owned_by() {
	owner="$1"
	durable_profile_owned_by "$owner" || return 1
	[ -z "$RUNTIME_DIR" ] || directory_owned_by "$RUNTIME_DIR" "$owner"
}

if ! profile_paths_exist; then
	echo "No demo environment to remove."
	exit 0
fi

owner="$(config_owner || true)"
if ! profile_owned_by "$owner"; then
	echo "refusing: demo profile state is not fully owned by this harness; leaving every target untouched." >&2
	exit 1
fi

if [ -z "$GR" ]; then
	echo "refusing to remove demo state: gr not found, so a demo daemon cannot be stopped safely" >&2
	exit 1
fi

# A real `gr list` creates the ephemeral runtime directory without a harness
# marker. Re-establish it after durable authorization but before any `gr` call.
# Atomic final-component mkdir refuses a concurrently-created unowned target.
if [ -n "$RUNTIME_DIR" ] && ! path_exists "$RUNTIME_DIR"; then
	mkdir -p "$(dirname "$RUNTIME_DIR")"
	if (umask 077 && mkdir "$RUNTIME_DIR"); then
		if ! (umask 077 && printf '%s\n' "$owner" > "$RUNTIME_DIR/$OWNER_FILE"); then
			echo "refusing to remove demo state: could not mark reconstructed runtime state" >&2
			exit 1
		fi
	elif ! directory_owned_by "$RUNTIME_DIR" "$owner"; then
		echo "refusing to remove demo state: runtime state appeared without matching ownership" >&2
		exit 1
	fi
fi
if ! profile_fully_owned_by "$owner"; then
	echo "refusing to remove demo state: ownership changed before daemon access" >&2
	exit 1
fi

# Every gr process reloads its config. Copy it once, then authenticate the copy
# against the harness proof and use only that immutable pathname for all CLI
# calls below. A concurrent config rewrite can therefore only make us refuse;
# it cannot rebind list/purge to another data directory between invocations.
CONFIG_SNAPSHOT="$(mktemp /tmp/graith-demo-config.XXXXXX)" || {
	echo "refusing to remove demo state: could not create a verified config snapshot" >&2
	exit 1
}
cleanup_snapshot() {
	rm -f "$CONFIG_SNAPSHOT"
}
trap cleanup_snapshot EXIT
trap 'exit 130' INT
trap 'exit 143' TERM
if ! (umask 077 && cp "$CONFIG_FILE" "$CONFIG_SNAPSHOT") ||
	! config_file_proven_by "$CONFIG_SNAPSHOT" "$CONFIG_DIR/$CONFIG_PROOF_FILE" "$owner"; then
	echo "refusing to remove demo state: config changed while authorization was established" >&2
	exit 1
fi
demo_gr() {
	"$GR" --config "$CONFIG_SNAPSHOT" "$@"
}

# Enumerate live and soft-deleted sessions before the first purge. Any list
# failure or marker mismatch therefore refuses while all sessions are intact.
if ! active_sessions="$(demo_gr list -q --json 2>/dev/null)"; then
	echo "refusing to remove demo state: could not enumerate live sessions" >&2
	exit 1
fi
if ! deleted_sessions="$(demo_gr list --deleted -q --json 2>/dev/null)"; then
	echo "refusing to remove demo state: could not enumerate deleted sessions" >&2
	exit 1
fi

parse_session_ids() {
	sessions_json="$1"
	sessions="$(printf '%s\n' "$sessions_json" | tr -d '[]"' | tr -d '[:space:]' | tr ',' '\n')"
	while IFS= read -r session_id; do
		[ -z "$session_id" ] && continue
		[ "${#session_id}" -eq 8 ] || return 1
		case "$session_id" in
			*[!0-9a-f]*) return 1 ;;
		esac
		printf '%s\n' "$session_id"
	done <<< "$sessions"
}

# Validate both complete enumerations before the first irreversible purge.
if ! active_ids="$(parse_session_ids "$active_sessions")" ||
	! deleted_ids="$(parse_session_ids "$deleted_sessions")"; then
	echo "refusing to remove demo state: invalid session ID from list" >&2
	exit 1
fi
if ! profile_fully_owned_by "$owner"; then
	echo "refusing to remove demo state: ownership changed before session purge" >&2
	exit 1
fi

# Purge immutable IDs rather than names: a live and deleted session may legally
# share a name. Quiet JSON is a simple array of hexadecimal IDs.
for sessions in "$active_ids" "$deleted_ids"; do
	while IFS= read -r session_id; do
		[ -z "$session_id" ] && continue
		echo "==> purging session ID: $session_id"
		if ! demo_gr purge "$session_id" -y -f >/dev/null 2>&1; then
			echo "refusing to remove demo state: purge failed for $session_id" >&2
			exit 1
		fi
	done <<< "$sessions"
done

echo "==> stopping demo daemon"
if ! demo_gr daemon stop >/dev/null 2>&1; then
	echo "refusing to remove demo state: daemon stop failed" >&2
	exit 1
fi

# Markers authorize each recursive removal independently. Check once more at
# the destructive boundary in case anything changed while the daemon stopped.
if ! profile_fully_owned_by "$owner"; then
	echo "refusing to remove demo state: ownership changed before directory quarantine" >&2
	exit 1
fi

# Rename inside a subshell whose cwd is the opened parent directory. This binds
# every operation to that parent inode even if an ancestor path is exchanged.
# Revalidate after the atomic rename, delete from inside the quarantined inode,
# and finish with non-recursive rmdir. A replacement at the original name is
# detected and preserved rather than becoming a new rm -rf target.
quarantine_and_remove_owned_directory() {
	dir="$1" owner="$2" kind="$3"
	parent="${dir%/*}"
	base="${dir##*/}"
	quarantine=".$base.graith-removing-$owner"
	(
		cd -P "$parent" || return 1
		source_path="./$base"
		quarantine_path="./$quarantine"
		if path_exists "$quarantine_path"; then
			echo "refusing to remove demo state: quarantine target already exists for $dir" >&2
			return 1
		fi
		if [ "$kind" = "config" ]; then
			config_directory_owned_by "$source_path" "$owner" || return 1
		else
			directory_owned_by "$source_path" "$owner" || return 1
		fi
		mv "$source_path" "$quarantine_path" || return 1
		if [ "$kind" = "config" ]; then
			config_directory_owned_by "$quarantine_path" "$owner" || return 1
		else
			directory_owned_by "$quarantine_path" "$owner" || return 1
		fi
		if path_exists "$source_path"; then
			echo "refusing to remove demo state: replacement appeared at $dir; preserving it" >&2
			return 1
		fi
		(
			cd -P "$quarantine_path" || return 1
			find . -xdev -depth ! -name . -delete
		) || return 1
		rmdir "$quarantine_path" || return 1
		if path_exists "$source_path"; then
			echo "refusing to remove demo state: replacement appeared at $dir; preserving it" >&2
			return 1
		fi
	)
}

echo "==> removing $CONFIG_DIR and $DATA_DIR"
quarantine_and_remove_owned_directory "$CONFIG_DIR" "$owner" config || exit 1
quarantine_and_remove_owned_directory "$DATA_DIR" "$owner" data || exit 1
if [ -n "$RUNTIME_DIR" ]; then
	quarantine_and_remove_owned_directory "$RUNTIME_DIR" "$owner" runtime || exit 1
fi

echo "Demo environment removed."
